public class Task2 {
    public void execute() {
        System.out.println("=== Завдання 2 (FOR) ===");
        for (int h = 0; h <= 2; h++) {
            for (int m = 0; m < 60; m++) {
                System.out.println(h + " h " + m + " min");
            }
        }
        System.out.println("--------------------------------------------------\n");
    }
}